## Data loading and processing
"""

# ╔═╡ d1e2d0a8-dc6d-47d3-8465-51df28b35443
md"""
Let's import the dataset and take a quick glance
"""

# ╔═╡ c5b94fb2-edf4-4c8f-921c-a3509a5608c0
begin
	table = PalmerPenguins.load()
	data = DataFrame(table)
	first(data, 5)
end

# ╔═╡ c05bf3d0-db9f-42d9-9bcb-fdb4d6b4640a
schema(data)

# ╔═╡ 180af1f5-1eb4-46c7-a322-202aa1a82c02
md"""
**Scientific Types** are an important part of the ML workflows in Julia. Most of the types are derived correctly, but some need to be "converted" for them to be properly treated. See here for more information about [ScientificTypes.jl](https://juliaai.github.io/ScientificTypes.jl/dev/).

In this case, the crucial step is to replace both the `Textual` types with a `Multiclass` scitype, as the `species` (e.g. _Adelie_) and `sex` are categorical attributes.

`Multiclass` is unordered, as opposed to `Count`, which considers that the available discrete values are ordered and there's meaning in such order.

`Missing` is a built-in type to identify missing values. There are some models that can work with missing data. In our case, we'll drop them for simplicity.
"""

# ╔═╡ 16cb84d4-aee4-4857-8d03-d2d04783b00f
dropmissing!(data)

# ╔═╡ 26aaa910-3390-4869-b03b-ef58be865ef9
md"""
### Fixing randomisation

To ensure consistent and repeatable results, we can instantiate a `StableRNG` object as follows:
"""

# ╔═╡ 1f19ef34-b4de-48ba-9bfc-0d948b0f1fca
myRNG = StableRNG(123)

# ╔═╡ 9ed0e7b3-d8bb-4d46-b3f8-b5ccbf8c151d
md"""
### Loading the dataset into a Matrix

Then we _unpack_ the dataset (`table` or `DataFrame`) by providing the target class (using the `isequal` function) and an `RNG` object.

We also need to provide a list of pairs `column_name => ScientificType`  to make the data compatible with the models in `MLJ`. As stated before, we'll convert the `Textual` fields into `Multiclass`.
"""

# ╔═╡ 5d1a0127-a255-419c-9b11-d146ceeb3870
y, X = unpack(data, ==(:species); rng=myRNG, :sex => Multiclass, :island => Multiclass, :species=>Multiclass);

# ╔═╡ 34457505-1893-4d8c-a1bf-8e2d31f53394
md"""
We can now check the scheme of the dataset (i.e. `X`):
"""

# ╔═╡ 218e7e24-0bb9-403a-80ef-a30a965d3848
schema(X)

# ╔═╡ d45c4495-8082-4553-923a-9a58658fe6a3
md"""
## Modelling

`MLJ` has lots of models, so the recommended approach is to _search_ through them using the `models` function. It can search using a string, or better yet, only look for models _matching_ our dataset:
"""

# ╔═╡ a5a57085-7261-4a3c-b589-be5b1e881ca8
models(matching(X, y))

# ╔═╡ 92052378-04fd-42b6-ae39-f0641a3af32e
md"""
Now, we can _load_ our chosen classifier using the `@load` macro. Let's use a Random Forest from `BetaML`. We can use `verbosity=0` to supress warnings and output messages:
"""

# ╔═╡ 0abb8965-a598-4c76-900e-8650b704470e
Classifier = @load RandomForestClassifier pkg=BetaML verbosity=0

# ╔═╡ 7f0a6f74-e82f-48ab-b903-369fa95d83e8
md"""
After loading the model, we now need to _instantiate_ it:
"""

# ╔═╡ 5fa323a1-ff1c-4312-a363-c12334886ce4
model = Classifier()

# ╔═╡ 6e7e4053-f035-4bb8-be65-90455135b6b2
md"""
And then make a `machine` out of the instantiated model, passing the data it will be trained on:
"""

# ╔═╡ a63342bb-6103-4722-95a1-f5b12631a908
mach = machine(model, X, y)

# ╔═╡ 84087236-caf5-4b9e-964f-55a28771d98e
md"""
### Train / test split
Let's split the data as 0.3/0.7 for training and testing respectively.
We can use different containers, or instead use a list of indices for partition.
Let's do the latter:
"""

# ╔═╡ cdab0a40-b85c-4388-984b-655c3c9782dd
train, test = partition(eachindex(y), 0.7, rng=myRNG)

# ╔═╡ 06f5dd4a-c90d-4ab3-8ce4-ed7637bc878b
md"""
Notice that the partition is made as a list of indices, so when we refer to the training subset, we need to specify it using index notation: $y_{train}$ is `y[train]`
"""

# ╔═╡ 1a7de6b9-c6eb-46a3-b9e7-ba680963a130
md"""
Now we can fit the model using `fit!`. It will _update_ the `mach` object.
Since we have a list of indices, we need to use the `rows` kwarg:
"""

# ╔═╡ 3f1903b8-0731-4e6f-b14e-1355016918be
fit!(mach, rows=train)

# ╔═╡ e100bec7-e618-4953-aa43-f577b34b2998
md"""
## Evaluating the model
We can now make all sort of calculations for evaluating our model. Some measures are of course already included:
"""

# ╔═╡ d004334c-24e0-4c29-a530-0ec3d6c21f42
evaluate(model, X, y, measures=[accuracy])

# ╔═╡ f0137fac-29af-453c-aab7-b1511808f1fd
md"""
This is evaluating the model (not the machine!) given the data. If we want to measure how our `machine` is doing, we can also do that using the `predict` function:
"""

# ╔═╡ efd56eec-b108-4267-ba92-5e03a200dbdb
yhat = predict(mach, X[test, :])

# ╔═╡ 65e87cf4-5ccc-4524-80ce-66157ec40037
md"""
And then calculate another metric given both $\hat{y}$ and $y$. All metrics in `MLJ` work like this:

$$metric(\hat{y}, y)$$
"""

# ╔═╡ b548717a-aaf7-4e3b-a370-bc4c4be8cfd9
mean(brier_loss(yhat, y[test]))

# ╔═╡ 2b34111e-6487-4422-b592-8e8ff0086d46
md"""
which is the average Brier loss of our prediction vector.
"""

